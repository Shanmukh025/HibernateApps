package com.klu.S67Spring;

public class Student {
	
	public Student(){}
	
	private int sno;
	private String sname;
	
	public void setSno(int sno)
	{
		this.sno=sno;
	}
	
	public int getSno()
	{
		return sno;
	}
	
	public void setSname(String sname)
	{
		this.sname=sname;
	}
	public String getSname()
	{
		return sname;
	}
}
